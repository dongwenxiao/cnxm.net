const languageConfig = {
    zh: { name: "中文", flag: "🇨🇳" },
    en: { name: "English", flag: "🇬🇧" },
    ja: { name: "日本語", flag: "🇯🇵" },
    ru: { name: "Русский", flag: "🇷🇺" },
    pt: { name: "Português", flag: "🇵🇹" }
};

// Current language (default: English)
let currentLang = localStorage.getItem('preferred_language') || 'en';

// Initialize i18n
function initI18n() {
    // Set current language
    setLanguage(currentLang);
    
    // Create language selector if not exists
    if (!document.getElementById('languageSelector')) {
        createLanguageSelector();
    }
}

// Create language selector dropdown
function createLanguageSelector() {
    // Use existing container from HTML if available, otherwise create one
    let langSelector = document.getElementById('languageSelector');
    if (!langSelector) {
        const navbar = document.querySelector('.nav-wrapper');
        if (!navbar) return;
        langSelector = document.createElement('div');
        langSelector.className = 'language-selector';
        langSelector.id = 'languageSelector';
        const mobileToggle = navbar.querySelector('.mobile-toggle');
        navbar.insertBefore(langSelector, mobileToggle);
    }
    
    const currentLangConfig = languageConfig[currentLang];
    
    langSelector.innerHTML = `
        <button class="lang-btn" id="langBtn">
            <span class="lang-flag">${currentLangConfig.flag}</span>
            <span class="lang-name">${currentLangConfig.name}</span>
            <i class="fas fa-chevron-down"></i>
        </button>
        <div class="lang-dropdown" id="langDropdown">
            ${Object.keys(languageConfig).map(lang => `
                <button class="lang-option ${lang === currentLang ? 'active' : ''}" data-lang="${lang}">
                    <span class="lang-flag">${languageConfig[lang].flag}</span>
                    <span class="lang-name">${languageConfig[lang].name}</span>
                    ${lang === currentLang ? '<i class="fas fa-check"></i>' : ''}
                </button>
            `).join('')}
        </div>
    `;
    
    // Add event listeners
    const langBtn = document.getElementById('langBtn');
    const langDropdown = document.getElementById('langDropdown');
    
    langBtn.addEventListener('click', (e) => {
        e.stopPropagation();
        langDropdown.classList.toggle('show');
    });
    
    // Close dropdown when clicking outside
    document.addEventListener('click', () => {
        langDropdown.classList.remove('show');
    });
    
    // Language option click handlers
    document.querySelectorAll('.lang-option').forEach(option => {
        option.addEventListener('click', (e) => {
            e.stopPropagation();
            const lang = option.dataset.lang;
            setLanguage(lang);
            langDropdown.classList.remove('show');
        });
    });
}

// Set language
function setLanguage(lang) {
    if (!translations[lang]) {
        console.error(`Language ${lang} not found`);
        return;
    }
    
    currentLang = lang;
    localStorage.setItem('preferred_language', lang);
    
    // Update HTML lang attribute
    document.documentElement.lang = lang;
    
    // Translate all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                element.innerHTML = translations[lang][key];
            }
        }
    });
    
    // Update language selector if exists
    const langBtn = document.getElementById('langBtn');
    if (langBtn) {
        const langConfig = languageConfig[lang];
        langBtn.innerHTML = `
            <span class="lang-flag">${langConfig.flag}</span>
            <span class="lang-name">${langConfig.name}</span>
            <i class="fas fa-chevron-down"></i>
        `;
    }
    
    // Update active state in dropdown
    document.querySelectorAll('.lang-option').forEach(option => {
        const optionLang = option.dataset.lang;
        if (optionLang === lang) {
            option.classList.add('active');
            if (!option.querySelector('.fa-check')) {
                option.innerHTML += '<i class="fas fa-check"></i>';
            }
        } else {
            option.classList.remove('active');
            const checkIcon = option.querySelector('.fa-check');
            if (checkIcon) checkIcon.remove();
        }
    });
    
    // Dispatch custom event for language change
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
}

// Get translation for a key
function t(key) {
    return translations[currentLang][key] || key;
}

// Export functions
window.i18n = {
    init: initI18n,
    setLanguage: setLanguage,
    t: t,
    currentLang: () => currentLang
};

// Auto-initialize when DOM is ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initI18n);
} else {
    initI18n();
}